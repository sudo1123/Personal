import turtle
myPen=turtle.Pen()
myPen.pencolor("blue")
myPen.pensize(4)
for i in range(4):
    myPen.forward(50)
    myPen.right(90)
myPen.penup()
myPen.goto(70,0)
myPen.pendown()
for i in range(4):
    myPen.forward(50)
    myPen.right(90)
myPen.penup()
myPen.goto(140,0)
myPen.pendown()
for i in range(4):
    myPen.forward(50)
    myPen.right(90)
